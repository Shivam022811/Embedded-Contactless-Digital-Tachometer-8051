#include "buzzer.h"

/******************************************************************************
 * Initialize Buzzer
 ******************************************************************************/

void Buzzer_Init(void)
{
    BUZZER = 0;
}

/******************************************************************************
 * Turn Buzzer ON
 ******************************************************************************/

void Buzzer_On(void)
{
    BUZZER = 1;
}

/******************************************************************************
 * Turn Buzzer OFF
 ******************************************************************************/

void Buzzer_Off(void)
{
    BUZZER = 0;
}
#include "delay.h"

/******************************************************************************
 * Delay in Microseconds
 ******************************************************************************/

void Delay_us(unsigned int us)
{
    while(us--)
    {
        ;
    }
}

/******************************************************************************
 * Delay in Milliseconds
 ******************************************************************************/

void Delay_ms(unsigned int ms)
{
    unsigned int i;

    while(ms--)
    {
        for(i = 0; i < 120; i++)
        {
            ;
        }
    }
}